package edu;

public class Quick {
    public static void main(String[] args) {
        System.out.println("");
        System.out.println(" \"123\" ");
        System.out.println(" \"\"123\"\" ");
        System.out.println(" '123' ");
        System.out.println(" ''123'' ");
        System.out.println(" `123` ");
        System.out.println(" ``123`` ");

        System.out.println();

        System.out.println(Double.toString(2.0 - 1.1));
        System.out.println(Float.toString(2.0f - 1.1f));
    }
}
